    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
     String nama = jTextFieldNama.getText();
    String alamat = jTextFieldAlamat.getText();
    String hubungan = jTextFieldHubungan.getText();
    String telepon = jTextFieldTelepon.getText();

    if (nama.isEmpty() || alamat.isEmpty() || hubungan.isEmpty() || telepon.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try (java.sql.Connection conn = Koneksi.connect()) {
        String sql = "INSERT INTO Alamat (nama, alamat, hubungan, telepon) VALUES (?, ?, ?, ?)";
        java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setString(1, nama);
        pstmt.setString(2, alamat);
        pstmt.setString(3, hubungan);
        pstmt.setString(4, telepon);
        pstmt.executeUpdate();

        // Tambahkan data ke atas JTable
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.insertRow(0, new Object[]{nama, alamat, hubungan, telepon});

        JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan ke database!", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        clearFields();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonTambahActionPerformed

    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // Ambil data dari JTextField
    String nama = jTextFieldNama.getText();
    String alamat = jTextFieldAlamat.getText();
    String hubungan = jTextFieldHubungan.getText();
    String telepon = jTextFieldTelepon.getText();

    // Validasi input
    if (nama.isEmpty() || alamat.isEmpty() || hubungan.isEmpty() || telepon.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Ambil indeks baris yang dipilih
    int selectedRowIndex = jTable1.getSelectedRow();
    if (selectedRowIndex == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Tidak ada baris yang dipilih!", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Perbarui data di database
    try (java.sql.Connection conn = Koneksi.connect()) {
        String sql = "UPDATE Alamat SET nama = ?, alamat = ?, hubungan = ?, telepon = ? WHERE id = ?";
        java.sql.PreparedStatement pstmt = conn.prepareStatement(sql);

        // Set parameter berdasarkan input
        pstmt.setString(1, nama);
        pstmt.setString(2, alamat);
        pstmt.setString(3, hubungan);
        pstmt.setString(4, telepon);
        pstmt.setInt(5, selectedRowIndex + 1); // Asumsikan id dimulai dari 1

        pstmt.executeUpdate();

        // Perbarui data di JTable
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setValueAt(nama, selectedRowIndex, 0);
        model.setValueAt(alamat, selectedRowIndex, 1);
        model.setValueAt(hubungan, selectedRowIndex, 2);
        model.setValueAt(telepon, selectedRowIndex, 3);

        javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Informasi", javax.swing.JOptionPane.INFORMATION_MESSAGE);

        // Bersihkan form
        clearFields();

        // Nonaktifkan tombol Simpan
        jButtonSimpan.setEnabled(false);
        jButtonTambah.setEnabled(true);
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Gagal memperbarui data: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jButtonEksporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEksporActionPerformed
    // Gunakan JFileChooser untuk memilih lokasi file CSV
    javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
    fileChooser.setDialogTitle("Pilih Lokasi untuk Menyimpan File CSV");
    fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("CSV Files", "csv"));

    int result = fileChooser.showSaveDialog(this);

    if (result == javax.swing.JFileChooser.APPROVE_OPTION) {
        java.io.File selectedFile = fileChooser.getSelectedFile();

        // Pastikan file berformat .csv
        if (!selectedFile.getName().toLowerCase().endsWith(".csv")) {
            selectedFile = new java.io.File(selectedFile.getAbsolutePath() + ".csv");
        }

        try (java.io.FileWriter writer = new java.io.FileWriter(selectedFile)) {
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();

            // Tulis header
            writer.write("Nama,Alamat,Hubungan,Telepon\n");

            // Tulis data
            for (int i = 0; i < model.getRowCount(); i++) {
                writer.write(model.getValueAt(i, 0) + "," +
                             model.getValueAt(i, 1) + "," +
                             model.getValueAt(i, 2) + "," +
                             model.getValueAt(i, 3) + "\n");
            }

            javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diekspor ke file: " + selectedFile.getName(), "Informasi", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengekspor data: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_jButtonEksporActionPerformed

    private void jButtonImporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonImporActionPerformed
    JFileChooser fileChooser = new JFileChooser();
    fileChooser.setDialogTitle("Pilih File CSV untuk Diimpor");
    fileChooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("CSV Files", "csv"));

    int result = fileChooser.showOpenDialog(this);

    if (result == JFileChooser.APPROVE_OPTION) {
        File selectedFile = fileChooser.getSelectedFile();

        try (BufferedReader reader = new BufferedReader(new FileReader(selectedFile))) {
            javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();

            // Kosongkan tabel sebelum impor
            model.setRowCount(0);

            // Lewati header
            reader.readLine();

            // Baca data baris per baris
            String line;
            while ((line = reader.readLine()) != null) {
                String[] data = line.split(",");
                model.insertRow(0, data); // Tambahkan di atas
            }

            JOptionPane.showMessageDialog(this, "Data berhasil diimpor dari file: " + selectedFile.getName(), "Informasi", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengimpor data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_jButtonImporActionPerformed

