    private void filterTableByName(String query) {
    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    javax.swing.table.DefaultTableModel filteredModel = new javax.swing.table.DefaultTableModel(
        new Object[]{"Nama", "Alamat", "Hubungan", "Telepon"}, 0);

    // Iterasi melalui semua baris di model
    for (int i = 0; i < model.getRowCount(); i++) {
        String nama = model.getValueAt(i, 0).toString().toLowerCase();

        // Jika nama mengandung query, tambahkan ke model yang difilter
        if (nama.contains(query.toLowerCase())) {
            filteredModel.addRow(new Object[]{
                model.getValueAt(i, 0),
                model.getValueAt(i, 1),
                model.getValueAt(i, 2),
                model.getValueAt(i, 3)
            });
        }
    }

    // Set model tabel menjadi model yang sudah difilter
    jTable1.setModel(filteredModel);
}
    
